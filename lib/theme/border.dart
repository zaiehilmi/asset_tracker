class AppBorder {
  const AppBorder._();

  /// Radius 0 - Untuk komponen tanpa sudut bulat
  static const none = 0.0;

  /// Radius 4 - Untuk butang kecil atau chip
  static const sm = 4.0;

  /// Radius 8 - Untuk kad dan komponen sederhana
  static const md = 8.0;

  /// Radius 12 - Untuk komponen standard iOS
  static const lg = 12.0;

  /// Radius 16 - Untuk modal dan sheet
  static const xl = 16.0;

  /// Radius 24 - Untuk floating action button
  static const xxl = 24.0;

  /// Radius 100 - Untuk bentuk bulat penuh (pill shape)
  static const full = 100.0;
}
